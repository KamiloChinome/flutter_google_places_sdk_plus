  /// Parses a place type string (snake_case or SCREAMING_SNAKE_CASE) into
  /// a [PlaceType] value. Returns [PlaceType.OTHER] for unrecognized types,
  /// so that new types added by the API won't crash the client.
  factory PlaceType.fromJson(String name) {
    final upper = name.toUpperCase();
    return _valuesByUpperName[upper] ?? OTHER;
  }
}

/// Lookup map for fast O(1) parsing of place type strings.
final _valuesByUpperName = {
  for (final placeType in PlaceType.values) placeType.name: placeType,
};

final placeTypeValuesMap = {
  for (final placeType in PlaceType.values) placeType.name: placeType,
};

extension PlaceTypeDescriptor on PlaceType {
  String get value => name;
}

extension PlaceTypeEnumParser on String {
  PlaceType? toPlaceType() {
    if (toUpperCase() == 'NULL') {
      return null;
    }
    return placeTypeValuesMap[toUpperCase()];
  }
}
